#include "filler.h"

int		vector_calc(int h, int w)
{
	int		ret;

	if (h < 0)
		h *= -1;
	if (w < 0)
		w *= -1;
	ret = (h * h) + (w * w);
	return ((int)square_root(ret, 5));
}